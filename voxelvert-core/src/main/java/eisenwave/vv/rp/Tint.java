package eisenwave.vv.rp;

public enum Tint {
    /** No tint */
    NONE,
    /** Tint which draws values from {@code colormap/grass.png} */
    GRASS,
    /** Tint which draws values from {@code colormap/foliage.png} */
    FOLIAGE,
    /** Constant tint for blocks such as spruce or birch */
    CONSTANT
}
